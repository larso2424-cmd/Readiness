export function getTranslations(lang: LangCode) {
  return translations[lang] ?? translations.en
}

export function getLang(): LangCode {
  if (typeof window === 'undefined') return 'en'
  return (localStorage.getItem('studyready_lang') as LangCode) ?? 'en'
}

export function setLang(lang: LangCode) {
  localStorage.setItem('studyready_lang', lang)
  // Also set cookie so server can read it (1 year expiry)
  document.cookie = `studyready_lang=${lang}; path=/; max-age=${60 * 60 * 24 * 365}; SameSite=Lax`
}

export function getLangFromCookie(value: string | null): LangCode {
  if (!value) return 'en'
  const valid: LangCode[] = ['en', 'de', 'nl', 'fr', 'es']
  return valid.includes(value as LangCode) ? (value as LangCode) : 'en'
}
